'use strict';
//=============================================================================
/**
 * module dependencies
 */
//=============================================================================
const
  app = require('./app'),
  http = require('http');
//=============================================================================
/**
 * server instance
 */
//=============================================================================
const server = http.createServer(app);
//=============================================================================
/**
 * module variables
 */
//=============================================================================
const
  port = app.get('port'),
  env = app.get('env');
//=============================================================================
/**
 * bind server to port
 */
//=============================================================================
server.listen(port, () => {
  console.log(`Server up on port ${port} in ${env} mode`);
});
//=============================================================================
/**
 * exported object
 */
//=============================================================================
if(require.main != module) {
  module.exports = server;
}
//=============================================================================
